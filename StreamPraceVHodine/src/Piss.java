public class Piss {
    private int year;
    private String subject;
    private String winner;

    public Piss(int year, String subject, String winner) {
        this.year = year;
        this.subject = subject;
        this.winner = winner;
    }

    public int getYear() {
        return year;
    }

    public String getSubject() {
        return subject;
    }

    public String getWinner() {
        return winner;
    }
}
